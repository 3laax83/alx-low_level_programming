#include "3-calc.h"

/**
 * op_add - function to add 2 numbers.
 * @a: num1
 * @b: num2
 * Return: the sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function to subtract 2 numbers.
 * @a: num1
 * @b: num2
 * Return: the sub
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - to find the product of 2 numbers
 * @a: num1
 * @b: num2
 * Return: the product
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - to find the result of division of 2 numbers
 * @a: num1
 * @b: num2
 * Return: the product
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - to find the modulo of 2 numbers
 * @a: num1
 * @b: num2
 * Return: the mod
 */

int op_mod(int a, int b)
{
	return (a % b);
}
